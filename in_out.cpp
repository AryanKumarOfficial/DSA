#include <iostream>
using namespace std;

int main()
{
    /**
     * Insertion operator - '<<'
     * Excertion operator - '>>'
     */
     int num1, num2;
    cout << "Enter the value of num 1 " << endl;
    cin >> num1;

    cout << "Enter the value of num 2 " << endl;
    cin >> num2;

    cout << "Sum is " << num1 + num2;
}